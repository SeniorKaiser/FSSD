#include <stdio.h>

// Функция для ввода размера массива с проверкой корректности
int Len_Mas () {
    
    int size;
    do {
        
        printf("Введите размер массива: ");
        scanf("%d", &size);
        
        // Проверка, что размер массива больше 1
        if (size <= 1) {
            printf("\n\nРазмер массива должен быть больше 1");
        }
    } while (size <= 1); // Повторять ввод, пока не будет введен корректный размер
    return size;
}

// Функция для ввода элементов массива
void Input_Mas(int Mas[], int size) {
    
    printf("Введите элементы массива:\n");
    
    // Цикл для ввода каждого элемента массива
    for (int i = 0; i < size; i++) {
        
        printf("Mas[%d] = ", i);
        scanf("%d", &Mas[i]);
    }
}

// Функция для нахождения наибольшего общего делителя (НОД) элементов массива
int Max_del(int Mas[], int size) {
    
    // Находим максимальный элемент в массиве
    int max_el = Mas[0];
    for (int i = 1; i < size; i++) {
        
        if (Mas[i] > max_el) {
            max_el = Mas[i];
        }
    }
    
    int max_del = 0;
    // Перебираем возможные делители от максимального элемента до 1
    for (int j = max_el; j >= 1; j--) {
        int flag = 1; // Флаг, указывающий что текущий j - общий делитель
        
        // Проверяем, делится ли каждый элемент массива на j
        for (int i = 0; i < size; i++) {
            if (Mas[i] % j != 0) {
                flag = 0; // Если хотя бы один элемент не делится, сбрасываем флаг
                break;
            }
        }
        // Если нашли общий делитель, возвращаем его
        if (flag == 1) {
            return j;
        }
    }
    return 1; // Если не нашли других делителей, возвращаем 1
}

// Главная функция программы
int main() {    
    // Получаем размер массива
    int size = Len_Mas();
    // Объявляем массив
    int Mas[size];
    // Вводим элементы массива
    Input_Mas(Mas, size);
    // Находим наибольший общий делитель
    int res = Max_del(Mas, size);
    // Выводим результат
    printf("Наибольший общий делитель: %d\n", res);
    return 0;
}
