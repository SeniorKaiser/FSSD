#include <stdio.h>

// Массив строк для чисел от 0 до 9 (в именительном падеже)
const char* ones[] = {
    "", "один", "два", "три", "четыре", "пять",
    "шесть", "семь", "восемь", "девять"
};

// Массив строк для чисел от 10 до 19
const char* teens[] = {
    "десять", "одиннадцать", "двенадцать", "тринадцать", "четырнадцать",
    "пятнадцать", "шестнадцать", "семнадцать", "восемнадцать", "девятнадцать"
};

// Массив строк для десятков (20, 30, ..., 90).
const char* tens[] = {
    "", "", "двадцать", "тридцать", "сорок",
    "пятьдесят", "шестьдесят", "семьдесят", "восемьдесят", "девяносто"
};

// Функция преобразования числа (1-99) в текстовый формат
void print_number_words(int k) {
    if (k < 10) {
        // Числа 1-9: берем из массива ones
        printf("%s", ones[k]);
    }
    else if (k < 20) {
        // Числа 10-19: берем из массива teens
        printf("%s", teens[k - 10]);
    }
    else {
        // Числа 20-99: комбинируем десятки и единицы
        printf("%s", tens[k / 10]);  // Десятки
        if (k % 10 != 0) {
            printf(" %s", ones[k % 10]);  // Единицы (если есть)
        }
    }
}

// Функция выбора правильной формы слова "год"
const char* get_year_word(int k) {
    // Правила русского языка для выбора окончания:
    if (k % 10 == 1 && k % 100 != 11)  // 1, 21, 31... но не 11
        return "год";
    else if ((k % 10 >= 2 && k % 10 <= 4) && !(k % 100 >= 12 && k % 100 <= 14))  // 2-4, 22-24... но не 12-14
        return "года";
    else  // Все остальные случаи: 5-20, 25-30...
        return "лет";
}

int main() {
    // Перебор всех чисел от 1 до 99
    for (int k = 1; k <= 99; k++) {
        printf("Мне ");
        print_number_words(k);  // Вывод числа словами
        const char* year_word = get_year_word(k);  // Получение правильной формы слова
        printf(" %s\n", year_word);  // Завершение фразы
    }
    return 0;
}
