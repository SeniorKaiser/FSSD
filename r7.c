#include <stdio.h>
#include <stdlib.h>

#define N 1000  // Определение максимального размера матрицы

// Функция ввода матрицы
// matrix - указатель на массив из N элементов (на строку матрицы)
// m - указатель на переменную для хранения количества строк
// n - указатель на переменную для хранения количества столбцов
void Enter_Matrix(int (*matrix)[N], int *m, int *n)
{
    printf("\n Введи размерности матрицы (строки столбцы): ");
    scanf("%d %d", m, n);
    
    // Проверка, не превышают ли введенные размеры максимальный размер N
    if (*m > N || *n > N) {
        printf("Ошибка: размеры превышают максимальный размер %d\n", N);
        exit(1);  // Аварийное завершение программы при ошибке
    }
    
    printf(" Введи матрицу:\n");
    
    // Двойной цикл для ввода элементов матрицы
    for (int i = 0; i < *m; i++)       // i - индекс строки
    {
        for (int j = 0; j < *n; j++)   // j - индекс столбца
        {
            // Ввод элемента матрицы по адресу matrix[i][j]
            // Используется оператор & для получения адреса ячейки
            scanf("%d", &matrix[i][j]);
        }
    }
}

// Функция вывода матрицы на экран
// matrix - указатель на массив из N элементов (на строку матрицы)
// m - количество строк матрицы
// n - количество столбцов матрицы
void Print_Matrix(int (*matrix)[N], int m, int n)
{
    printf("\n Матрица – результат:\n");
    
    // Двойной цикл для вывода элементов матрицы
    for (int i = 0; i < m; i++)        // i - индекс строки
    {
        for (int j = 0; j < n; j++)    // j - индекс столбца
        {
            // Вывод элемента матрицы с форматированием (ширина 3 символа)
            printf("%3d ", matrix[i][j]);
        }
        printf("\n");  // Переход на новую строку после вывода всех элементов строки
    }
}

// Функция для поиска и вывода строк, содержащих заданное число
// Array - указатель на массив из N элементов (на строку матрицы)
// m - количество строк матрицы
// n - количество столбцов матрицы
void str_of_matr(int (*Array)[N], int m, int n) {
    int main_num;  // Переменная для хранения искомого числа
    
    printf("\nВведите число: ");
    scanf("%d", &main_num);  // Ввод искомого числа

    // Поиск по всем строкам матрицы
    for (int i = 0; i < m; i++) {
        // Флаг, указывающий, найдено ли число в текущей строке
        int found = 0;
        
        // Поиск числа в текущей строке
        for (int j = 0; j < n; j++) {
            if (Array[i][j] == main_num) {  // Если элемент равен искомому числу
                found = 1;  // Устанавливаем флаг "найдено"
                break;       // Прерываем внутренний цикл (число уже найдено)
            }
        }
        
        // Если число найдено в текущей строке, выводим всю строку
        if (found) {
            for (int j = 0; j < n; j++) {
                printf("%d ", Array[i][j]);  // Поэлементный вывод строки
            }
            printf("\n");  // Переход на новую строку после вывода всей строки
        }
    }
}

// Главная (основная) функция программы
int main() {
    // Объявление матрицы максимального размера NxN
    // Все элементы размещаются в стеке
    int matr[N][N];
    
    // Переменные для хранения фактических размеров матрицы
    int m, n;  // m - количество строк, n - количество столбцов
    
    // Вызов функции ввода матрицы (передаем адрес матрицы и адреса m, n)
    Enter_Matrix(matr, &m, &n);
    
    // Вызов функции вывода матрицы (передаем матрицу и значения m, n)
    Print_Matrix(matr, m, n);
    
    // Вызов функции поиска строк по заданному числу
    str_of_matr(matr, m, n);
    
    return 0;  // Успешное завершение программы
}
