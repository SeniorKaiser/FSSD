#include <stdio.h>
#define N 5  // Определение максимального размера матрицы

// Функция для ввода матрицы с клавиатуры
void Enter_Matrix(int Array[][N], int *m, int *n)
{
    int i, j;  // Переменные для циклов
    printf("\n Введи размерности матрицы:");  // Запрос размеров матрицы
    scanf("%d %d", m, n);  // Считывание количества строк и столбцов
    printf(" Введи матрицу :\n");  // Приглашение для ввода элементов
    for (i = 0; i < *m; i++)  // Цикл по строкам
        for (j = 0; j < *n; j++)  // Цикл по столбцам
            scanf("%d", &Array[i][j]);  // Считывание элемента матрицы
}

// Функция для вывода матрицы на экран 
void Print_Matrix(int Array[][N], int m, int n)
{
    int i, j;  // Переменные для циклов
    printf("\n Матрица – результат :\n");  // Заголовок вывода
    for (i = 0; i < m; i++)  // Цикл по строкам
    {
        for (j = 0; j < n; j++)  // Цикл по столбцам
            printf("%3d ", Array[i][j]);  // Вывод элемента с форматированием
        printf("\n");  // Переход на новую строку после каждой строки матрицы
    }
}

// Функция для поиска и вывода строк, содержащих заданное число
void str_of_matr(int Array[][N], int m, int n) {
    int main_num;  // Переменная для хранения искомого числа
    printf("\nВведите число: ");  // Запрос числа для поиска
    scanf("%d", &main_num);  // Считывание числа
    for (int i = 0; i < m; i++) {  // Цикл по строкам матрицы
        for (int j = 0; j < n; j++) {  // Цикл по столбцам матрицы
            if (Array[i][j] == main_num) {  // Проверка совпадения элемента с искомым числом
                int index = i;  // Сохранение индекса найденной строки
                for (int column = 0; column < n; column++) {  // Цикл по элементам найденной строки
                    printf("%d ", Array[index][column]);  // Вывод элементов строки (добавлен пробел)
                }
                printf("\n");  // Переход на новую строку после вывода всей строки
                break;  // Прерывание внутреннего цикла после нахождения числа в строке
            }
        }
    }
}

// Главная функция программы
int main() {
    int matr[N][N];  // Объявление матрицы максимального размера NxN
    int m, n;        // Переменные для хранения фактических размеров матрицы
    Enter_Matrix(matr, &m, &n);  // Вызов функции ввода матрицы
    Print_Matrix(matr, m, n);    // Вызов функции вывода матрицы
    str_of_matr(matr, m, n);     // Вызов функции поиска строк по числу
    return 0;  // Завершение программы
}